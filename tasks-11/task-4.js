// Використовуючи проміси потрібно змінити цикл так, щоб числа виводилися в строгій послідовності від 0 до 10. 
// Наприклад, якщо виведення нуля займає 4 секунди, а одиниці 2 секунди, то одиниця повинна дочекатися виведення  нуля і тільки потім 
// почати свій відлік (щоб дотримуватися послідовності).
// Для розв’язку задачі необзідно застосувати задану функцію delay(i, time), яка повертає проміс, який резолвиться поточним значенням 
// числа-лічильника циклу і, яке виводиться через час time мілісекунд.

const delay = (i, time) => new Promise(resolve => setTimeout(() => resolve(i), time));
async function showNumbers() {
    for (let i = 0; i <= 10; i++) {
        const result = await delay(i, Math.random() * (100 - 0 + 1) + 0);
        console.log(result);
    }
}
showNumbers();

